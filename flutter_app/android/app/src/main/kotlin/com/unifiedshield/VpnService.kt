                restartVpn()
            }
        }
        return START_STICKY
    }

    private fun setupVpn() {
        if (isRunning) return

        val builder = Builder()
            .setSession("UnifiedShield")
            .setMtu(1380)
            .addAddress("172.19.0.1", 24)
            .addDnsServer("1.1.1.1")
            .addDnsServer("8.8.8.8")
            .addDnsServer("223.5.5.5")  // AliDNS
            .addDnsServer("119.29.29.29")  // Tencent DNSPod

        // Route all traffic through VPN
        builder.addRoute("0.0.0.0", 0)
        builder.addRoute("::", 0)

        // Exclude own app from VPN to prevent loops
        try {
            builder.addDisallowedApplication("com.unifiedshield")
        } catch (e: Exception) {
            Log.w(TAG, "Could not exclude own app from VPN", e)
        }

        vpnInterface = builder.establish()
        if (vpnInterface == null) {
            Log.e(TAG, "Failed to create VPN interface")
            stopSelf()
            return
        }

        isRunning = true
        showNotification("Connected", currentCoreName)

        startPacketProcessing()
    }

    private fun startPacketProcessing() {
        thread = Thread({
            val fileDescriptor = vpnInterface?.fileDescriptor ?: return@Thread
            val inputStream = FileInputStream(fileDescriptor)
            val outputStream = FileOutputStream(fileDescriptor)
            val packet = ByteBuffer.allocate(32767)

            // Connect to Rust daemon via Unix abstract socket
            val daemonSocket = Socket()
            try {
                // Abstract namespace Unix socket - connect to local Rust daemon
                // In practice this uses LocalSocket on Android
                // Here we use TCP loopback as the Rust daemon listens on localhost
                daemonSocket.connect(InetAddress.getByName("127.0.0.1"), 9191)
                daemonSocket.tcpNoDelay = true
                val daemonOutput = daemonSocket.getOutputStream()
                val daemonInput = daemonSocket.getInputStream()

                var lastSpeedUpdate = System.currentTimeMillis()
                var bytesDownloaded: Long = 0
                var bytesUploaded: Long = 0

                while (isRunning && !Thread.interrupted()) {
                    // Read packet from TUN interface
                    val length = inputStream.read(packet.array())
                    if (length > 0) {
                        packet.limit(length)

                        // Forward to Rust daemon
                        daemonOutput.write(packet.array(), 0, length)
                        daemonOutput.flush()

                        bytesUploaded += length.toLong()

                        // Read response from daemon
                        val responseLength = daemonInput.read(packet.array())
                        if (responseLength > 0) {
                            // Write back to TUN interface
                            outputStream.write(packet.array(), 0, responseLength)
                            bytesDownloaded += responseLength.toLong()
                        }

                        packet.clear()

                        // Update speed stats every 2 seconds
                        val now = System.currentTimeMillis()
                        if (now - lastSpeedUpdate > 2000) {
                            val elapsed = (now - lastSpeedUpdate) / 1000.0
                            uploadSpeed = (bytesUploaded / elapsed).toLong()
                            downloadSpeed = (bytesDownloaded / elapsed).toLong()
                            bytesUploaded = 0
                            bytesDownloaded = 0
                            lastSpeedUpdate = now
                            updateNotification()
                        }
                    }
                }
            } catch (e: Exception) {
                Log.e(TAG, "Packet processing error", e)
            } finally {
                try {
                    daemonSocket.close()
                } catch (_: Exception) {}
            }
        }, "UnifiedShield-TUN")
        thread?.start()
    }

    private fun stopVpn() {
        isRunning = false
        thread?.interrupt()
        thread = null
        try {
            vpnInterface?.close()
        } catch (_: Exception) {}
        vpnInterface = null
        stopForeground(STOP_FOREGROUND_REMOVE)
        stopSelf()
    }

    private fun restartVpn() {
        stopVpn()
        setupVpn()
    }

    private fun createNotificationChannel() {
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            val channel = NotificationChannel(
                NOTIFICATION_CHANNEL_ID,
                "UnifiedShield VPN",
                NotificationManager.IMPORTANCE_LOW
            ).apply {
                description = "VPN connection status"
                setShowBadge(false)
            }
            val manager = getSystemService(NotificationManager::class.java)
            manager.createNotificationChannel(channel)
        }
    }

    private fun showNotification(status: String, coreName: String) {
        val disconnectIntent = Intent(this, UnifiedShieldVpnService::class.java).apply {
            action = ACTION_DISCONNECT
        }
        val pendingIntent = PendingIntent.getService(
            this, 0, disconnectIntent,
            PendingIntent.FLAG_UPDATE_CURRENT or PendingIntent.FLAG_IMMUTABLE
        )

        val notification = Notification.Builder(this, NOTIFICATION_CHANNEL_ID)
            .setContentTitle("UnifiedShield - $status")
            .setContentText("Core: $coreName")
            .setSmallIcon(android.R.drawable.ic_lock_lock)
            .addAction(
                Notification.Action.Builder(
                    null, "Disconnect", pendingIntent
                ).build()
            )
            .setOngoing(true)
            .build()

        startForeground(NOTIFICATION_ID, notification)
    }

    private fun updateNotification() {
        val manager = getSystemService(NotificationManager::class.java)
        val downloadStr = formatSpeed(downloadSpeed)
        val uploadStr = formatSpeed(uploadSpeed)

        val disconnectIntent = Intent(this, UnifiedShieldVpnService::class.java).apply {
            action = ACTION_DISCONNECT
        }
        val pendingIntent = PendingIntent.getService(
            this, 0, disconnectIntent,
            PendingIntent.FLAG_UPDATE_CURRENT or PendingIntent.FLAG_IMMUTABLE
        )

        val notification = Notification.Builder(this, NOTIFICATION_CHANNEL_ID)
            .setContentTitle("UnifiedShield - Connected")
            .setContentText("$currentCoreName | ↓$downloadStr ↑$uploadStr")
            .setSmallIcon(android.R.drawable.ic_lock_lock)
            .addAction(
                Notification.Action.Builder(
                    null, "Disconnect", pendingIntent
                ).build()
            )
            .setOngoing(true)
            .build()

        manager.notify(NOTIFICATION_ID, notification)
    }

    private fun formatSpeed(bytesPerSec: Long): String {
        return when {
            bytesPerSec < 1024 -> "$bytesPerSec B/s"
            bytesPerSec < 1024 * 1024 -> "${"%.1f".format(bytesPerSec / 1024.0)} KB/s"
            else -> "${"%.1f".format(bytesPerSec / (1024.0 * 1024.0))} MB/s"
        }
    }

    override fun onDestroy() {
        stopVpn()
        super.onDestroy()
    }

    override fun onRevoke() {
        stopVpn()
        super.onRevoke()
    }
