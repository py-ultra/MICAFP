    override fun onStartCommand(intent: Intent?, flags: Int, startId: Int): Int {
        when (intent?.action) {
            ACTION_CONNECT -> {
                currentCoreName = intent.getStringExtra("core_name") ?: "UnifiedShield"
                setupVpn()
            }
            ACTION_DISCONNECT -> {
                stopVpn()
            }
            ACTION_SWITCH_CORE -> {
